package co.com.sofka.model.jobtitle;

public class JobTitleModel {

    private String jobTitle;
    private String jobDescription;
    private String jobEspecification;
    private String note;

    public String getJobTitle() {
        return jobTitle;
    }

    public void setJobTitle(String jobTitle) {
        this.jobTitle = jobTitle;
    }

    public String getJobDescription() {
        return jobDescription;
    }

    public void setJobDescription(String jobDescription) {
        this.jobDescription = jobDescription;
    }

    public String getJobEspecification() {
        return jobEspecification;
    }

    public void setJobEspecification(String jobEspecification) {
        this.jobEspecification = jobEspecification;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
